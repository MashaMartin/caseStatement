package caseproject;

public class caseclass {

	public static void main(String[] args) {
		// Switch condition
		String string ="one";
		switch (string) {
		case "one":
			System.out.println("Its 1");
			break;
		case "two":
			System.out.println("Its 2");
			break;
		case "three":
			System.out.println("Its 3 ");
			break;
		default:
			System.out.println("Its not one of the numbers");
		}
	}

}
